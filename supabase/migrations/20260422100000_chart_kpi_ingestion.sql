drop policy if exists "chart_daily_kpis_select_assigned_hospitals" on analytics.chart_daily_kpis;
create policy "chart_daily_kpis_select_assigned_hospitals"
  on analytics.chart_daily_kpis
  for select
  to authenticated
  using (
    exists (
      select 1
      from core.users u
      where u.id::text = auth.uid()::text
        and (
          lower(coalesce(u.role, 'member')) = 'admin'
          or u.hospital_id = chart_daily_kpis.hospital_id
        )
    )
  );

