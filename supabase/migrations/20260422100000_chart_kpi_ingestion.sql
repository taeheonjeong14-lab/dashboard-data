create or replace view analytics.chart_kpis_period_view as
select
  date_trunc('week', metric_date)::date as period_start_date,
  'week'::text as period_type,
  hospital_id,
  chart_type,
  sum(sales_amount) as sales_amount,
  sum(visit_count) as visit_count,
  sum(new_customer_count) as new_customer_count
from analytics.chart_daily_kpis
group by 1, 2, 3, 4
union all
select
  date_trunc('month', metric_date)::date as period_start_date,
  'month'::text as period_type,
  hospital_id,
  chart_type,
  sum(sales_amount) as sales_amount,
  sum(visit_count) as visit_count,
  sum(new_customer_count) as new_customer_count
from analytics.chart_daily_kpis
group by 1, 2, 3, 4
union all
select
  metric_date as period_start_date,
  'day'::text as period_type,
  hospital_id,
  chart_type,
  sales_amount,
  visit_count,
  new_customer_count
from analytics.chart_daily_kpis;

grant select on table analytics.chart_kpis_period_view to authenticated, service_role;
